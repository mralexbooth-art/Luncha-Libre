// Phase 8 — every ingredient carries a 6-stat nutrition object (with derived
// calories), plus the Phase 4 fields (tags, prepBonus for chop math, special,
// cuttable flag, quickEat flag, cookTimeAdds flag).
//
// Schema:
//   nutrition: { protein, carbs, fat, fiber, sugar, sodium, calories }
//   prepBonus: { attack, health, cookTime }  -- chop result modifies attack/health DIRECTLY
//   special:   { id, name, trigger, position, description }  -- Phase 4 specials, layered with Phase 8 thresholds

const cal = (n) => n.protein + n.carbs + (n.fat * 2) + n.fiber + n.sugar
const nut = (p, c, f, fb, sug, sod) => {
  const out = { protein: p, carbs: c, fat: f, fiber: fb, sugar: sug, sodium: sod }
  out.calories = cal(out)
  return out
}

