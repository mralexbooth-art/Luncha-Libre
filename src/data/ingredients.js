// Phase 9.x ingredient compression: heavy/structural ingredients keep their
// own slot in compressing cookware. Everything else (aromatics, spices, dairy,
// herbs, small veg) compresses into a single shared slot after 1 turn cooking.
const NON_COMPRESSIBLE = new Set([
  'beef-chuck', 'pork', 'chicken', 'chorizo',     // hearty proteins
  'potato', 'plantain',                            // bulky starches
  'tortilla', 'corn-masa', 'flour',                // structural starches
])

// Phase 9 burn system: each ingredient gets a burnLimit (heat units it can
// absorb before burning) inferred from its tags. Coolants subtract from a pot's
// heatUnits when placed (cream/butter/lard). Null burnLimit = ingredient
// effectively never burns (fats, quick-eats).
const inferBurnLimit = (ing) => {
  if (ing.quickEat) return null
  const tags = new Set(ing.tags)
  if (tags.has('fat') || tags.has('lard') || tags.has('crema') || tags.has('butter')) return null
  if (tags.has('cilantro') || tags.has('herb')) return 1
  if (tags.has('spice') || tags.has('chili') || tags.has('pepper') || tags.has('aromatic')) return 2
  if (tags.has('sugar') || tags.has('sweet') || tags.has('chocolate')) return 2
  if (tags.has('cheese')) return 3
  if (tags.has('dairy')) return 4
  if (tags.has('vegetable') || tags.has('fresh') || tags.has('legume')) return 3
  if (tags.has('citrus') || tags.has('acid')) return 5
  if (tags.has('starch') || tags.has('grain') || tags.has('flour') || tags.has('masa')) return 4
  if (tags.has('protein') || tags.has('red-meat') || tags.has('white-meat') || tags.has('animal')) return 5
  return 4
}
const COOLANT_OVERRIDES = { crema: 2, butter: 1, lard: 1 }
for (const ing of INGREDIENTS) {
  if (ing.burnLimit === undefined) ing.burnLimit = inferBurnLimit(ing)
  if (COOLANT_OVERRIDES[ing.id] != null) ing.coolant = COOLANT_OVERRIDES[ing.id]
  // Phase 9.x compression flag — defaults to true (compressible) unless
  // explicitly listed as a heavy/structural ingredient.
  if (ing.compressible === undefined) {
    ing.compressible = !NON_COMPRESSIBLE.has(ing.id) && !ing.quickEat
  }
}

